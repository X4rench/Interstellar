export type Gender = 'male' | 'female'

/**
 * Темпоральная привязка персонажа. Используется в composePersona()
 * для генерации блока «Твоя эпоха» — что персонаж знает/не знает,
 * как реагирует на современные реалии (телефоны, ОСАГО, и т.п.).
 */
export interface CharacterEra {
  bornYear: number              // 1856
  diedYear: number | null       // 1939, null если ещё жив (для авторов-современников)
  /** Короткое описание эпохи: «Вена конца 19 — начала 20 в.» */
  context?: string
  /** Что хорошо знает (3-5 главных тем). */
  knewWell?: string[]
  /** Чего не знал (главные современные понятия). Опционально — если не задано, используется общее правило. */
  didntKnow?: string[]
}

export interface Character {
  id: string
  name: string
  description: string
  category: string
  iconType: string
  gradientKey: string
  tags: string[]
  messages: number
  rating: number
  isNew?: boolean
  isNSFW?: boolean
  firstMessage: string
  persona: string
  userCreated?: boolean
  // Поле для кастомных персонажей (Задача 3): пол влияет на promt-обёртку
  gender?: Gender
  // Поле для кастомных персонажей (Задача 3.4): индивидуальный градиент
  // карточки. Если задан — используется вместо cardBg[gradientKey].
  customGradient?: readonly [string, string]
  // Поле для кастомных персонажей (Задача 5): сырое описание характера
  // от пользователя (до сборки в шаблон). persona содержит финальный шаблон.
  rawPersona?: string
  // Кастомная картинка-аватар для пользовательских персонажей. Абсолютный
  // file:// путь в FileSystem.documentDirectory/avatars/. Если задан —
  // отрисовывается вместо SVG-иконки (см. CharacterIcon.tsx).
  avatarUri?: string

  // ── Persona v2: improved immersion ────────────────────────────────────
  // Темпоральная привязка — годы жизни и эпоха. Если задана —
  // composePersona() добавит блок про эпоху, и персонаж не будет
  // делать вид что знает про современные реалии (ОСАГО, интернет и т.п.).
  era?: CharacterEra
  // Фирменный «ход» персонажа — как он типично реагирует на
  // эмоциональные/проблемные темы. Например для Фрейда:
  // «переводишь разговор на детство и сны». Для Сократа: «отвечаешь
  // встречным вопросом». Это самое сильное средство immersion.
  signature?: string
  // Личные убеждения. Делают персонажа НЕ зеркалом — он может не
  // согласиться, спорить, защищать свою точку зрения.
  opinions?: string
}

