/**
 * Реестр настроений (mood) — модификаторов стиля общения с персонажем.
 *
 * Применяется и при создании кастомного персонажа (selectedMoods из
 * CreateScreen) и при смене стиля в ChatScreen (MoodPickerModal).
 *
 * Текстовые суффиксы добавляются к persona перед отправкой на backend
 * через composePersona(). Backend получает уже скомпонованный typazh.
 */
export interface MoodDef {
  id: string;
  label: string;
  /** Краткое описание для модалки выбора */
  hint?: string;
  /** Доступен только для NSFW-персонажей с подтверждённым возрастом 18+ */
  requires18?: boolean;
}

